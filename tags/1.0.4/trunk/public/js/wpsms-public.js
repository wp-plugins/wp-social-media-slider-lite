var LazyLoader;

(function( $ ) {
	'use strict';

	LazyLoader = {

		loadPosts: function( helper ) {
	        $.ajax({
	            url: ajaxurl,
	            dataType: 'json',
	            data: {
	                action: "wpsms_lazy_load_posts"
	            }
	        }).done( function(response) {

				LazyLoader.renderPosts( response.posts, helper );

				if( response.time_to_update == '1' ) {
					LazyLoader.refreshCache();
				}
	        });
		},

		refreshCache: function() {
	        $.ajax({
	            url: ajaxurl,
	            dataType: 'json',
	            data: {
	                action: "wpsms_refresh_cache"
	            }
	        }).done(function(response) {
	        	console.log( response.status );
	        });			
		},

		activateMagnificPopup: function( helper ) {
			$('.wpsms-magnific').magnificPopup({
				type: 'image',
				closeOnContentClick: true,
				image: {
					verticalFit: false
				},
				callbacks: {
					open: function() {
						helper.pause();
					},
					close: function() {
						helper.resume();
					}
				}
			});
		},

		renderPosts: function( posts, helper ) {

			// Schedule the adding of the posts
			setTimeout( function() {

				var slider  = helper.slider;
				var owl     = helper.data;
				var is_ie_8 = $('body').hasClass( 'is-ie-8');

				// First, fade out the spinners
				$(".wpsms .lazy-load-spinner").each(function( index ) {
					$(this).fadeOut( 300 );
				});

				// First we need to replace the current carousel elements with
				// the first post.
				slider.trigger( 'replace.owl.carousel', [ $( posts.shift() ) ] );
		
				// Add the posts
	            $.each( posts, function(index, value) {
	                slider.trigger( 'add.owl.carousel', [ $( value ) ] );
	            });

	            // Refresh the carousel
	            owl.refresh();

	            slider.trigger( 'to.owl.carousel', [ '0', '0' ] );

	            setTimeout( function() {

	            	var delay = 0;

	            	$(".wpsms .owl-item").not('.active').show().css( 'visibility', 'hidden' );

		            // Fade the new posts in incrementally
		            $(".wpsms .owl-item.active").each(function( index ) {
		            	delay = ( parseInt( index, 10 ) + 1 ) * 200;

		            	if ( !is_ie_8 ) {
			            	$(this).attr( 'data-unique', delay ).delay( delay ).fadeIn( 800 ).promise().done( function() {
			            		if ( index == 0 ) {
				            		$(".wpsms .owl-item").not('.active').css( 'visibility', 'visible' );
				            	}
			            	});
		            	} else {
			            	$(this).attr( 'data-unique', delay ).delay( delay ).show();

		            		if ( index == 0 ) {
			            		$(".wpsms .owl-item").not('.active').css( 'visibility', 'visible' );
			            	}
		            	}

		            });

            		var controls = $('.wpsms .owl-controls');

            		// Then, fade in the prev and next elements
	            	if ( !is_ie_8 ) {
	            		controls.fadeIn( 300 );
	            	} else {
	            		controls.show();
	            	}

            		LazyLoader.activateMagnificPopup( helper );

					// Keep autoplay paused until it loads
					helper.resume();


	            }, 100);


	    		
			}, 300 );
		}


	};

})( jQuery );
